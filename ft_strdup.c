char	*ft_strdup(char *src)
{
	int i;
	int len;
	char *out;

	i = 0;
	len = ft_strlen(src);
	out = malloc(sizeof((*src) * (len)));
	while (i <= len)
	{
		out[i] = src[i];
		i++;
	}
	return (out);
}
