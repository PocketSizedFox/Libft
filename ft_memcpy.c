#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;
	char	*ptr;
	char	*ptr2;

	ptr = dst;
	ptr2 = (char *)src;
	i = -1;
	if (ptr == NULL && ptr2 == NULL)
		return (0);
	while (++i < n)
		*(ptr + i) = *(ptr2 + i);
	return (dst);
}
